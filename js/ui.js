export function renderGallery(media, { isEditing = false, isSelectionMode = false, selectedIds = new Set() } = {}) {
    let content;
    if (media.length === 0) {
        content = renderEmptyState(icons.gallery, "Thư viện trống", isEditing ? "Hãy tải lên media mới." : "Hiện chưa có ảnh, video hoặc âm thanh nào.");
    } else {
        const items = media.map(item => {
            let mediaElement;
            let mediaIcon = '';
            let containerAttributes = '';

            switch (item.type) {
                case 'video':
                    mediaElement = `<video src="${item.url}" class="w-full h-full object-cover" preload="metadata" ${!isEditing ? 'controls' : ''}></video>`;
                    if (isEditing) mediaIcon = icons.video;
                    break;
                case 'audio':
                     if (!isEditing) {
                        mediaElement = `<div class="w-full h-full bg-gray-800 flex flex-col items-center justify-center p-2">
                            ${icons.audio.replace('class="h-6 w-6 text-white"', 'class="h-10 w-10 text-gray-300"')}
                            <audio src="${item.url}" controls class="w-full mt-4"></audio>
                        </div>`;
                    } else {
                        mediaElement = `<div class="w-full h-full bg-gray-800 flex items-center justify-center">${icons.audio.replace('h-6 w-6', 'h-16 w-16')}</div>`;
                        mediaIcon = icons.audio;
                    }
                    break;
                default: // image
                    // Placeholder logic: Show data/no_image.png with img-pulse until real image loads
                    // If error, the main image stays hidden and placeholder remains visible with pulse (as requested)
                    mediaElement = `
                        <div class="relative w-full h-full bg-gray-200 dark:bg-gray-800 flex items-center justify-center overflow-hidden">
                            <img src="../data/no_image.png" class="absolute w-1/2 h-1/2 object-contain img-pulse opacity-40 transition-opacity duration-300" alt="Loading...">
                            <img 
                                src="${item.url}" 
                                alt="${item.caption || 'Gallery Image'}" 
                                onload="this.previousElementSibling.style.display='none'; this.style.opacity='1'"
                                onerror="this.style.display='none'; this.previousElementSibling.classList.remove('opacity-40'); this.previousElementSibling.classList.add('opacity-80')"
                                class="w-full h-full object-cover transform group-hover:scale-110 transition-transform duration-300 opacity-0 ${!isEditing ? 'cursor-pointer' : ''}" 
                            />
                        </div>
                    `;
                    if (!isEditing) containerAttributes = `data-lightbox-item="true" data-src="${item.url}"`;
                    break;
            }
            
            const isSelected = selectedIds.has(item.id);
            const selectionClass = isSelectionMode ? 'cursor-pointer' : (isEditing ? 'cursor-grab active:cursor-grabbing' : '');
            return `
            <div 
                key="${item.id}"
                data-id="${item.id}" 
                draggable="${isEditing && !isSelectionMode}"
                class="media-item group relative overflow-hidden rounded-lg shadow-lg aspect-square bg-gray-200 dark:bg-gray-700 ${selectionClass} ${isSelected ? 'ring-4 ring-offset-2 ring-indigo-500' : ''}" 
                ${containerAttributes}
                ${isSelectionMode ? `data-action="toggle-select-media"` : ''}
            >
                ${mediaElement}
                ${mediaIcon && `<div class="absolute top-3 left-3 p-1.5 bg-black/40 rounded-full">${mediaIcon}</div>`}
                <div class="absolute inset-0 bg-gradient-to-t from-black/70 via-black/20 to-transparent opacity-0 group-hover:opacity-100 transition-opacity duration-300 flex flex-col justify-end p-4">
                    <p class="text-white text-sm font-semibold transform translate-y-4 group-hover:translate-y-0 transition-transform duration-300">${item.caption || ''}</p>
                </div>
                ${isEditing ? `
                    ${isSelectionMode ? `
                        <div class="absolute top-2 left-2 text-white bg-black/30 rounded-full pointer-events-none">
                            ${isSelected ? icons.checkboxChecked : icons.checkbox}
                        </div>` 
                    : `
                    <div class="absolute top-2 right-2 flex space-x-2 opacity-0 group-hover:opacity-100 transition-opacity duration-300">
                        <button data-action="edit-media" data-id="${item.id}" class="p-2 bg-blue-500 rounded-full text-white hover:bg-blue-600 transition-transform hover:scale-110">${icons.pencil}</button>
                        <button data-action="delete-media" data-id="${item.id}" class="p-2 bg-red-500 rounded-full text-white hover:bg-red-600 transition-transform hover:scale-110">${icons.trash}</button>
                    </div>
                    `}
                ` : ''}
            </div>
        `}).join('');
        content = `<div class="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-4">${items}</div>`;
    }
    
    if (isEditing) {
        return content;
    }

    return `
    <div class="bg-white dark:bg-gray-800 rounded-xl shadow-md p-6">
        <h2 class="text-2xl font-bold text-gray-800 dark:text-gray-200 mb-6">Thư viện Media</h2>
        ${content}
    </div>`;
}

export function openLightbox(src) {
    if (document.getElementById('lightbox-container')) return;

    const lightboxHTML = `
        <div id="lightbox-container" class="fixed inset-0 bg-black/80 backdrop-blur-sm z-[100] flex items-center justify-center opacity-0 transition-opacity duration-300">
            <button id="lightbox-close" class="absolute top-4 right-4 text-white text-4xl z-10">&times;</button>
            <div class="relative w-full h-full flex items-center justify-center">
                <img id="lightbox-image" src="${src}" class="max-w-[90vw] max-h-[90vh] transition-transform duration-200" style="transform-origin: center center; cursor: grab;">
            </div>
        </div>
    `;
    document.body.insertAdjacentHTML('beforeend', lightboxHTML);

    const container = document.getElementById('lightbox-container');
    const image = document.getElementById('lightbox-image');
    
    requestAnimationFrame(() => { container.style.opacity = '1'; });

    let scale = 1, panning = false, pointX = 0, pointY = 0, start = { x: 0, y: 0 };
    
    const setTransform = () => { image.style.transform = `translate(${pointX}px, ${pointY}px) scale(${scale})`; };
    const onPointerDown = (e) => { e.preventDefault(); panning = true; start = { x: e.clientX - pointX, y: e.clientY - pointY }; image.style.cursor = 'grabbing'; };
    const onPointerUp = () => { panning = false; image.style.cursor = 'grab'; };
    const onPointerMove = (e) => { if (!panning) return; e.preventDefault(); pointX = e.clientX - start.x; pointY = e.clientY - start.y; setTransform(); };
    const onWheel = (e) => { e.preventDefault(); const xs = (e.clientX - pointX) / scale, ys = (e.clientY - pointY) / scale, delta = -e.deltaY; scale *= (1 + (delta > 0 ? 0.2 : -0.2)); scale = Math.min(Math.max(0.5, scale), 10); pointX = e.clientX - xs * scale; pointY = e.clientY - ys * scale; setTransform(); };

    image.addEventListener('mousedown', onPointerDown);
    container.addEventListener('mouseup', onPointerUp);
    container.addEventListener('mouseleave', onPointerUp);
    container.addEventListener('mousemove', onPointerMove);
    container.addEventListener('wheel', onWheel, { passive: false });
    
    const close = () => { container.style.opacity = '0'; setTimeout(() => container.remove(), 300); };

    document.getElementById('lightbox-close').addEventListener('click', close);
    container.addEventListener('click', (e) => { if (e.target === container) close(); });
}

function renderClassListTable(students, columns, { directEditMode = false } = {}) {
     if (students.length === 0 && !directEditMode) {
        return renderEmptyState(icons.users, "Danh sách lớp trống", "Hiện chưa có thông tin học sinh nào được thêm vào.");
    }

    const hasSttColumn = columns.some(col => col.label.toUpperCase() === 'STT');

    const headerHtml = `
        ${!hasSttColumn ? '<th scope="col" class="px-2 py-3 w-12 text-center bg-gray-200 dark:bg-gray-600">STT</th>' : ''}
        ${columns.map((col, idx) => `
            <th scope="col" class="px-6 py-3 relative group">
                ${directEditMode ? `
                    <div class="flex flex-col gap-1">
                        <input type="text" value="${col.label}" data-action="update-column-label" data-column-index="${idx}" class="bg-transparent border-b border-gray-400 focus:border-indigo-500 outline-none w-full text-center">
                        <button data-action="remove-column" data-column-index="${idx}" class="absolute -top-1 -right-1 p-1 bg-red-500 text-white rounded-full opacity-0 group-hover:opacity-100 transition-opacity">
                            ${icons.trash.replace('h-4 w-4', 'h-3 w-3')}
                        </button>
                    </div>
                ` : col.label}
            </th>
        `).join('')}
        ${directEditMode ? `
            <th scope="col" class="px-2 py-3 w-10 text-center">
                <button data-action="add-column-end" class="p-1.5 bg-indigo-500 text-white rounded-lg hover:bg-indigo-600 transition-colors" title="Thêm cột vào cuối">
                    ${icons.plus.replace('w-5 h-5', 'w-3.5 h-3.5')}
                </button>
            </th>
        ` : ''}
    `;

    const bodyHtml = students.map((student, index) => {
        const rowId = student.id;
        return `
        <tr 
            data-id="${rowId}" 
            data-index="${index}" 
            class="bg-white dark:bg-gray-800 even:bg-gray-50 dark:even:bg-gray-800/50 border-b dark:border-gray-700 group transition-colors duration-200" 
            ${directEditMode ? 'draggable="true" data-action="drag-row"' : ''}
        >
            ${!hasSttColumn ? `
                <td class="px-2 py-4 text-center font-medium text-gray-500 dark:text-gray-400 bg-gray-50 dark:bg-gray-700/30 w-12 relative overflow-hidden">
                    ${directEditMode ? `
                        <div class="stt-container w-full h-full flex items-center justify-center cursor-move group-hover:bg-indigo-50 dark:group-hover:bg-indigo-900/20">
                            <span class="stt-number block group-hover:hidden">${index + 1}</span>
                            <span class="stt-handle hidden group-hover:block text-indigo-500">${icons.dragHandle}</span>
                        </div>
                    ` : (index + 1)}
                </td>` : ''}
            ${columns.map(col => `
                <td class="px-6 py-4">
                    ${directEditMode ? `
                        <input type="text" value="${student[col.key] || ''}" data-action="update-cell" data-row-index="${index}" data-column-key="${col.key}" class="w-full bg-transparent border-none focus:ring-2 focus:ring-indigo-500 rounded px-1 transition-all">
                    ` : `
                        <div class="${col.label.toUpperCase() === 'HỌ VÀ TÊN' || col.key === 'name' ? 'font-semibold text-gray-900 dark:text-white' : ''}">
                            ${student[col.key] || ''}
                        </div>
                    `}
                </td>
            `).join('')}
            ${directEditMode ? `
                <td class="px-2 py-4 text-center">
                    <button data-action="remove-row" data-row-index="${index}" class="p-1.5 bg-red-100 text-red-600 rounded-lg hover:bg-red-200 opacity-0 group-hover:opacity-100 transition-opacity">
                        ${icons.trash.replace('h-4 w-4', 'h-4 w-4')}
                    </button>
                </td>
            ` : ''}
        </tr>
    `}).join('');
    
    return `
        <div id="classlist-table-container" class="overflow-x-auto relative shadow-md sm:rounded-lg bg-white dark:bg-gray-900 border border-gray-200 dark:border-gray-700">
            <table class="w-full text-sm text-left text-gray-500 dark:text-gray-400">
                <thead class="text-xs text-gray-700 uppercase bg-gray-100 dark:bg-gray-700 dark:text-gray-300">
                    <tr>${headerHtml}</tr>
                </thead>
                <tbody id="classlist-table-body">${bodyHtml}</tbody>
            </table>
            ${directEditMode ? `
                <div class="p-4 bg-gray-50 dark:bg-gray-800 border-t border-gray-200 dark:border-gray-700 flex justify-center">
                    <button data-action="add-row-end" class="inline-flex items-center gap-2 px-6 py-2 bg-indigo-600 text-white font-semibold rounded-lg hover:bg-indigo-700 transition-all shadow-sm">
                        ${icons.plus}<span>Thêm học sinh mới</span>
                    </button>
                </div>
            ` : ''}
        </div>`;
}


export function renderClassList(students, columns, { isEditing = false, directEditMode = false } = {}) {
    let content = '';
    
    if (isEditing) {
        content = renderEditToggle('classlist', directEditMode) + renderClassListTable(students, columns, { directEditMode });
    } else {
        content = renderClassListTable(students, columns);
    }

    if (isEditing) {
        return content;
    }

    return `
     <div class="bg-white dark:bg-gray-800 rounded-xl shadow-md p-6">
        <h2 class="text-2xl font-bold text-gray-800 dark:text-gray-200 mb-6">Danh sách Lớp</h2>
        ${content}
    </div>`;
}

function renderScheduleTable(schedule, { directEditMode = false } = {}) {
    const daysOfWeek = ['Thứ Hai', 'Thứ Ba', 'Thứ Tư', 'Thứ Năm', 'Thứ Sáu', 'Thứ Bảy'];
    const sessions = [{name: 'Sáng', key: 'morning'}, {name: 'Chiều', key: 'afternoon'}];
    const periods = [1, 2, 3, 4, 5];

    let tableHtml = `
    <div class="overflow-x-auto shadow-md sm:rounded-lg border border-gray-200 dark:border-gray-700">
      <table class="w-full border-collapse text-sm text-center text-gray-500 dark:text-gray-400 table-fixed min-w-[800px]">
        <thead class="text-xs text-gray-700 uppercase bg-gray-100 dark:bg-gray-700 dark:text-gray-300">
          <tr>
            <th class="px-2 py-3 border-r dark:border-gray-600 w-24 bg-gray-200 dark:bg-gray-600" colspan="2">Buổi / Tiết</th>
            ${daysOfWeek.map(day => `<th class="px-4 py-3">${day}</th>`).join('')}
          </tr>
        </thead>
        <tbody>
    `;
    sessions.forEach(session => {
        periods.forEach((period, periodIndex) => {
            tableHtml += `<tr class="bg-white dark:bg-gray-800 border-b dark:border-gray-700">`;
            if (periodIndex === 0) {
                tableHtml += `<td rowspan="5" class="px-2 py-4 font-bold text-gray-900 dark:text-white bg-gray-50 dark:bg-gray-700 border-r dark:border-gray-600 align-middle w-12">${session.name}</td>`;
            }
            tableHtml += `<td class="px-2 py-4 font-medium text-gray-600 dark:text-gray-400 border-r dark:border-gray-600 w-12 text-xs uppercase bg-gray-50/50 dark:bg-gray-700/30">T${period}</td>`;
            daysOfWeek.forEach(day => {
                const subject = schedule[day]?.[session.key]?.[periodIndex]?.subject || '';
                tableHtml += `
                    <td class="border-l dark:border-gray-700 p-0 align-middle">
                        <div class="w-full h-full min-h-[4rem] flex items-center justify-center">
                            ${directEditMode ? `
                                <textarea data-action="update-schedule-cell" data-day="${day}" data-session-key="${session.key}" data-period-index="${periodIndex}" class="w-full h-full min-h-[4rem] bg-transparent border-none focus:ring-2 focus:ring-indigo-500 rounded p-2 text-center resize-none">${subject}</textarea>
                            ` : `
                                <div class="p-2 font-semibold text-gray-900 dark:text-gray-100 whitespace-pre-line break-words leading-tight">
                                    ${subject}
                                </div>
                            `}
                        </div>
                    </td>`;
            });
            tableHtml += `</tr>`;
        });
    });
    tableHtml += `</tbody></table></div>`;
    return tableHtml;
}


export function renderSchedule(schedule, { isEditing = false, directEditMode = false } = {}) {
    let content = '';
    
    if (isEditing) {
        content = renderEditToggle('schedule', directEditMode) + renderScheduleTable(schedule, { directEditMode });
    } else {
        content = renderScheduleTable(schedule);
    }
    
    if (isEditing) {
        return content;
    }
    
    return `
        <div class="bg-white dark:bg-gray-800 rounded-xl shadow-md p-6">
            <h2 class="text-2xl font-bold text-gray-800 dark:text-gray-200 mb-6">Thời khóa biểu</h2>
            ${content}
        </div>
    `;
}
